/**
 * 专题二:计数器,累加求和
 * 1. 1+1+1...+1
 * 2. 1+2+3+4+....+99
 * 3. 2+4+6+....+98
 * 4. 1+3+5+....99
 * 5. 1-2+3-4+5.....-98+99
 * 6. 1^2+2^2+....+10^2
 * 7. 1+1/2+1/3+1/4+....+1/99
 * 8. 1/2+2/3+3/4+....+98/99
 * 9. 1.1+2.2+3.3+....+10.10+11.11+99.99
 */
public class Test02
{
    public static void main(String[] args) {
        int sum = 0;
        //1.1+1+1...+1
        for (int i=0;i<100;i++)
        {
            sum += 1;
        }
        System.out.println("100个1相加:"+sum);
        //2.1+2+3+4+....+99
        sum = 0;
        for (int i =1;i<=99;i++)
        {
            sum +=i;
        }
        System.out.println("1+2+3...+99= "+sum);

        //3. 2+4+6+....+98
        sum = 0;
        for (int i = 1;i<=(98/2);i++)
        {
            sum += 2*i;
        }
        System.out.println("2+4+6+....+98= "+sum);
        //4. 1+3+5+....99
        sum = 0;
        for (int i = 1;i<=99;i+=2)
        {
            sum += i;
        }
        System.out.println("1+3+5+....99= "+sum);

        //5. 1-2+3-4+5.....-98+99
        sum = 0;    //1+(-2)+3+(-4)+....+(-98)+99
        for (int i = 1;i<=99;i++)
        {
            //0==(i%2)?(-i):i; //偶数模2 == 0,则取负数
            sum += 0==(i%2)?(-i):i;
        }
        System.out.println("1-2+3-4+5.....-98+99= "+sum);

        //6. 1^2+2^2+....+10^2
        sum = 0;
        for (int i = 1;i <= 10;i++)
        {
            sum +=i*i;
        }
        System.out.println("1^2+2^2+....+10^2= "+sum);

        //7. 1+1/2+1/3+1/4+....+1/99
        double db = 0.0;
        for (int i = 1;i<=99;i++)
        {
            db += 1.0/i;
        }
        System.out.println("1+1/2+1/3+1/4+....+1/99= "+db);

        //8. 1/2+2/3+3/4+....+98/99
        db = 0.0;
        for (int i = 1;i<=98;i++)
        {
            db += 1.0*i/(i+1);
        }
        System.out.println("1/2+2/3+3/4+....+98/99= "+db);

        //9. 1.1+2.2+3.3+....+10.10+11.11+99.99
        db = 0.0;
        double b = 0.0;
        for (int i = 1; i <= 99; i++)
        {
            b= i<10? (0.1*i):(0.01*i);
            db = db+i+b;
        }
        System.out.println("1.1+2.2+3.3+....+10.10+11.11+99.99= "+db);
    }
}
